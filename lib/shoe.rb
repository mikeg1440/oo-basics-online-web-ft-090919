

class Shoe

  def initialize(brand)
    @brand = brand
  end

  def brand
    @brand
  end

  def color=(shoe_color)
    @color = shoe_color
  end

  def size=(shoe_size)
    @size = shoe_size
  end

  def material=(shoe_material)
    @material = shoe_material
  end

  def condition=(shoe_condition)
    @condition = shoe_condition
  end

  

end
